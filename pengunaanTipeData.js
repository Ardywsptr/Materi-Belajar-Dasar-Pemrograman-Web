//TIPE DATA NUMBERS :
let age = 24; //BILANGAN BULAT
console.log(typeof(age)); 

let height = 165.3; //BILANGAN DECIMAL 
console.log(typeof(height));

//TIPE DATA NUMBERS MENGGUNAKAN PERHITUNGAN ARITMATIKA :
